#!/usr/bin/env bash
set -euo pipefail

DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)/output"

if [[ ! -d "$DIR" ]]; then
    echo "error: output/ not found — run 'just generate' first" >&2
    exit 1
fi

copy_table() {
    local table="$1"
    local id_col="$2"
    local columns="$3"
    echo "  loading ${table}..."
    psql "$DATABASE_URL" \
        -c "ALTER TABLE library.${table} ALTER COLUMN ${id_col} SET GENERATED BY DEFAULT" \
        -c "\\copy library.${table} (${columns}) FROM '${DIR}/${table}.tsv' WITH (FORMAT text, DELIMITER E'\t')" \
        -c "ALTER TABLE library.${table} ALTER COLUMN ${id_col} SET GENERATED ALWAYS"
}

echo "Loading seed data..."

copy_table "struct_type" "struct_type_id" "struct_type_id, display_order, group_name, att_pub_ident, att_value"
copy_table "book"        "book_id"        "book_id, isbn, title, author_name"
copy_table "book_copy"   "book_copy_id"   "book_copy_id, book_id, status_id, barcode"
copy_table "member"      "member_id"      "member_id, member_ident, status_id, full_name, max_active_loans"
copy_table "loan"        "loan_id"        "loan_id, loan_ident, book_copy_id, member_id, dt_due, dt_returned"

echo "Resetting identity sequences..."
psql "$DATABASE_URL" << 'SQL'
SELECT setval(pg_get_serial_sequence('library.struct_type', 'struct_type_id'), MAX(struct_type_id)) FROM library.struct_type;
SELECT setval(pg_get_serial_sequence('library.book',        'book_id'),        MAX(book_id))        FROM library.book;
SELECT setval(pg_get_serial_sequence('library.book_copy',   'book_copy_id'),   MAX(book_copy_id))   FROM library.book_copy;
SELECT setval(pg_get_serial_sequence('library.member',      'member_id'),      MAX(member_id))      FROM library.member;
SELECT setval(pg_get_serial_sequence('library.loan',        'loan_id'),        MAX(loan_id))        FROM library.loan;
SQL

echo "Done."
